function sortByTemplate(array $array, array $pattern): array
{
    $result = [];
    $array = array_flip($array);

    foreach (array_flip($pattern) as $key => $val) {
        if (isset($array[$key])) {
            $result[$key] = $array[$key];
        }
    }

    return $result;
}

